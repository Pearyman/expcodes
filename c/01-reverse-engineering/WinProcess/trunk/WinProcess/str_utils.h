/************************************************************************/
/* 把[整数]转换成[等宽字符数组(unicode)]                                */
/************************************************************************/
TCHAR* toWChar(const int num);

/************************************************************************/
/* 把[不等宽字符数组]转换成[等宽字符数组(unicode)]                      */
/************************************************************************/
TCHAR* toWChar(const char* _char);

/************************************************************************/
/* 把[等宽字符数组(unicode)]转换成[不等宽字符数组]                      */
/************************************************************************/
char* toChar(const TCHAR* _wchar);

